<template>
  <div class="message-alert">
    <div
      class="alert alert-dismissible"
      :class="'alert-' + item.status"
      v-for="(item, num) in messages"
      :key="num"
    >
      {{ item.message }}
      <button
        type="button"
        class="close"
        @click="removeMessage(num)"
        aria-label="Close"
      >
        <span aria-hidden="true">&times;</span>
      </button>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      messages: [],
    };
  },
  methods: {
    removeMessage(num) {
      this.$store.dispatch('messageRemove', num);
    },
  },
  created() {
    this.messages = this.$store.state.Alert.messages;
  },
};
</script>

<style scope>
.message-alert {
  position: fixed;
  max-width: 50%;
  top: 56px;
  right: 20px;
  z-index: 1100;
}
</style>
